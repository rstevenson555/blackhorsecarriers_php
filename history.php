                <div style="margin-top:10px;" class="bodyText">
                    <span style="">Black Horse Carrier's roots go back to the
                        1880's to Des Plaines Illinois where a company called Des Plaines Transfer
                        operated with horses and wagons, moving then-common household items such as
                        pianos, moving trunks, billiard tables, as well as farm and nursery
                        products.</span>
                    <img src="images/Niedert1.jpg" alt="old truck" align="right" hspace="20" border="0">

                    <p>In 1925, sometime after Des Plaines
                        Transfer replaced its teams of horses with motorized trucks, Conrad W. Niedert
                        bought the business assets of Des Plaines Transfer, including two trucks, a
                        Master and a Ford.</p>

                    <p>Shortly after
                        the purchase Conrad's son Ferdinand also started operating a motorized trucking
                        business, and after Ferdinand's untimely death in the 1930's the two businesses
                        essentially merged into a single business.</p>

                    <p>Conrad's other
                        son, Ralph, entered the business in the late 1930's but elected to join the
                        Navy at the outset of WWII. After returning from
                        the war Ralph re-entered the trucking business along with his father
                        Conrad.</p>

                    <p>In the 1940's,
                        1950's and 1960's the business flourished in and around the Chicago area, and
                        in the late 1960's and early 1970's Ralph's two sons, Ralph Jr., and Gerry,
                        joined the business and worked at expanding the business into LTL Assembly
                        &amp; Distribution, Full-Service Truck Leasing and Contract Carriage, and to
                        get a foothold in the fast growing dedicated transportation business, made
                        possible by deregulation of the trucking industry, Gerry, in the early 1980's,
                        formed Black Horse Carriers, Inc. (so named to honor the famous 11<sup>th</sup>
                        Armored Cavalry Regiment and the 7<sup>th</sup> Cavalry Regiment, to which
                        Gerry was assigned while serving in the United States Army in
                        Vietnam).</p>

                    <p>By the mid
                        1980's the combined businesses had over 500 employees, six operating centers,
                        and a fleet of over 1,200 trucks, tractors and trailers.
                    </p>

                    <p>The
                        deregulation of the trucking industry had changed the face of the business, and
                        after working for a span of 5 decades Ralph Niedert, Sr. decided the economic
                        risks were becoming too great and it was probably time to retire.
                    </p>

                    <p>On September
                        30, 1986 the business was sold in its entirety to Leaseway Transportation
                        Corp., a publicly-held company based in Cleveland OH, operating with over
                        25,000 employees, 40,000 vehicles, and with extensive transportation,
                        warehousing and labor management operations throughout the country.</p>

                    <p>In the late
                        1980's Leaseway was the victim of an overly optimistic leveraged buyout and
                        failed in an attempt to become a privately-owned business, and was eventually
                        sold, much of which to the Penske Corporation, including the little that
                        remained of the old Niedert business, including Black Horse
                        Carriers.</p>

                    <p>Black Horse
                        Carriers operated in a very limited way while owned by
                        Leaseway and Penske, and eventually went into dormancy just before Ralph
                        Niedert resurrected the business in late 1998 in Tully NY, after getting
                        Penske's permission to again operate under the name of Black Horse
                        Carriers. Gerry Niedert joined him in April of 1999
                        in Center Valley PA, merging the two operations, and eventually adding
                        operations in the Midwest, solidly placing Black Horse Carriers back into the
                        business of dedicated transportation.
                    </p>

                    <p>Black Horse
                        Carriers today remains true to its original strategy:
                        providing high-value specialized transportation services by replacing the
                        private fleets of shippers whose businesses demand efficient and reliable
                        transportation services.</p>

